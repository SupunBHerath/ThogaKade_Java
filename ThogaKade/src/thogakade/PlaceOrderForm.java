    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (txtQty.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter the item qty..");
            txtQty.requestFocus();
        } else {

            String code = (String) txtItem.getSelectedItem();
            double OneItemTotal = Double.parseDouble(itemQtyPrice.getText()) * Double.parseDouble(txtQty.getText());
            FTotal += OneItemTotal;
            txtTotal.setText(FTotal + "0");
            DefaultTableModel dtm = (DefaultTableModel) itemTable.getModel();
//        dtm.setRowCount(0);
            Object[] rowData = {
                code, itemDes.getText(), txtQty.getText(), itemQtyPrice.getText(), OneItemTotal
            };
            dtm.addRow(rowData);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

