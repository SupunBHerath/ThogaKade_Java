    private void txtcustIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtcustIdActionPerformed
        Id = (String) txtcustId.getSelectedItem();
        try {
            Customer customer = CustomerController.searchCustomer(Id);

            if (customer != null) {
                txtName.setText(customer.getName());
                txtAddress.setText(customer.getAddress());
                txtSalary.setText(customer.getSalary() + "0");
            } else {
                JOptionPane.showMessageDialog(this, "Customer not found..");
            }

        } catch (ClassNotFoundException ex) {
            System.out.println("Driver s/w not found..");
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_txtcustIdActionPerformed

